// Код сделан по уроку 4. 9 Поиск в массиве


// Определяем массив и число для поиска
let array = [1, 2, 3, 4, 5]; // Вы можете изменить значения в этом массиве
let n = 7; // Число для поиска, также можно изменить на число в массиве и вывести Элемент найден:

let index = -1; // Изначально индекс установлен на -1 (означает, что элемент не найден)

// Цикл для поиска элемента
for (let i = 0; i < array.length; i++) {
  if (array[i] === n) {
    index = i; // Запоминаем индекс, если элемент найден
    break; // Выход из цикла, если элемент найден
  }
}

// Проверяем, был ли найден элемент
if (index !== -1) {
  console.log(`Элемент найден: ${index + 1}`); // Так как отсчет массива   начинается с 0, то добавляем 1 к индексу для вывода порядкового номера
} else {
  console.log(`Элемент не найден`); // Выводим сообщение о том, что элемент не найден
}



